    public FrmManterRevendedora(java.awt.Frame parent, boolean modal, Revendedora revendedora) {
        super(parent, modal);
        initComponents();
        
        BufferedImage image = null;
        try {
            image = ImageIO.read(this.getClass().getResource("/images/user_female.png"));
        } catch (IOException e) {
            
        }
        this.setIconImage(image);        
        
        if (revendedora == null){
            this.setTitle("Incluir Revendedora");
        }
        else {
            
            txtNome.setText(revendedora.getNome());
            txtCodigo.setText(revendedora.getId().toString());
            txtCpf.setText(revendedora.getCpf());
            txtDataNascimento.setText(revendedora.getDataNascimento());
            txtComissao.setText(revendedora.getComissao().toString());
            
            txtLogradouro.setText(revendedora.getLogradouro());
            txtBairro.setText(revendedora.getBairro());
            txtNumero.setText(revendedora.getNumero());
            txtComplemento.setText(revendedora.getComplemento());
            txtCep.setText(revendedora.getCep());
            txtCidade.setText(revendedora.getMunicipio());
            for (int i=1;i<cbEstado.getItemCount();i++){
                if (cbEstado.getItemAt(i).equals(revendedora.getUf())){
                    cbEstado.setSelectedIndex(i);
                    break;
                }
            }
            
            txtTelefone1.setText(revendedora.getTelefone1());
            txtTelefone2.setText(revendedora.getTelefone2());
            txtEmail.setText(revendedora.getEmail());
            
            ckDesativarRevendedora.setSelected(revendedora.getIcAtivo()==0?true:false);            
            this.setTitle("Alterar Revendedora");
        }
    }

