    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            Revendedora r = new Revendedora();
            r.setNome(txtNome.getText());
            if (txtCodigo.getText().length()!=0)
                r.setId(Integer.parseInt(txtCodigo.getText()));
            r.setCpf(txtCpf.getText());
            r.setDataNascimento(txtDataNascimento.getText());
            r.setComissao(Float.parseFloat(txtComissao.getText()));

            r.setLogradouro(txtLogradouro.getText());
            r.setBairro(txtBairro.getText());
            r.setNumero(txtNumero.getText());
            r.setComplemento(txtComplemento.getText());
            r.setCep(txtCep.getText());
            r.setMunicipio(txtCidade.getText());
            r.setUf(cbEstado.getSelectedItem().toString());
            
            r.setTelefone1(txtTelefone1.getText());
            r.setTelefone2(txtTelefone2.getText());
            r.setEmail(txtEmail.getText());

            r.setIcAtivo(ckDesativarRevendedora.isSelected()?0:1);
            
            ManterRevendedoraController.getInstance().save(r);
            this.dispose();
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

